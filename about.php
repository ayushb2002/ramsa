                <span class="fw-bold fs-4 d-flex justify-content-center text-capitalize">About the conference</span>
                <hr>
                <p class="text-justify">
                    The aim of this conference is to bring together learned mathematicians, scientists, engineers,
                    researchers from industry and research scholars working in the different areas of mathematics at a
                    common platform. The Conference provides an excellent opportunity to researchers, practitioners and
                    educators to present and discuss the recent innovations in mathematics for potential implementation
                    in sciences and engineering. It focuses on strengthening the existing results along with identifying
                    the practical challenges encountered with respect to various solutions adopted in the fields of
                    Mathematics and its Applications. Moreover, it will provide an opportunity to enhance collaboration
                    among researchers not only from the various parts of the country but also with those from abroad.
                    The scientific program will consist of Keynote/plenary/invited lectures and parallel sessions for
                    contributed presentations. The main emphasis will be on the vibrant implicational aspects of applied
                    mathematics for cultivating contemporary and advanced findings in multidimensional sphere of
                    engineering and sciences. The invited lectures and refereed contributed papers will be published in
                    the proceedings after due reviewing process by the committee of experts in various disciplines. The
                    conference proceedings of the previous series of RAMSA were published by <b> AIP (Scopus Indexed),
                        USA and Nova Publishers, USA.</b>
                </p>

